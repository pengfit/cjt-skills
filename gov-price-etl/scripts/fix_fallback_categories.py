def review_table():
    """打印 review 表（含当前 l3、置信度、source）"""
    conn = sqlite3.connect(str(DB_PATH))
    print(f'{"breed_clean":35} {"old_l3":10} {"new_l3":10} {"name_l2":20} {"old_src":20} {"old_conf":8}')
    print('-' * 120)
    cnt = 0
    for breed, new_l3, name_l2, reason in FIXES:
        row = conn.execute(
            "SELECT l3, source, confidence FROM breed_l3_map_v3 WHERE breed_clean=?",
            (breed,)
        ).fetchone()
        if row is None:
            print(f'  {breed!r:35} (no rule)         {new_l3} {name_l2:18}')
        else:
            print(f'  {breed!r:35} {row[0]!r:10} → {new_l3:8} {name_l2:18} (was {row[1]!r:15} conf={row[2]})  {reason}')
            cnt += 1
    print(f'\n共 {cnt} 条修正（来自 FIXES 表）')
    conn.close()


def apply_fixes():
    """写入 DB：UPDATE l3 + source=manual_v3.11 + confidence=1.0"""
    conn = sqlite3.connect(str(DB_PATH))
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    updated = 0
    not_found = 0
    for breed, new_l3, name_l2, reason in FIXES:
        # 取当前规则，更新 l3 + 强制 manual_v3.11 + conf=1.0
        cur = conn.execute(
            "SELECT rowid FROM breed_l3_map_v3 WHERE breed_clean=?", (breed,)
        ).fetchone()
        if cur is None:
            not_found += 1
            continue
        conn.execute(
            """
            UPDATE breed_l3_map_v3
            SET l3 = ?, source = ?, confidence = 1.0, updated_at = ?
            WHERE breed_clean = ?
            """,
            (new_l3, SOURCE, now, breed)
        )
        updated += 1
    conn.commit()
    conn.close()
    print(f'\n✓ updated {updated} rules')
    if not_found:
        print(f'  (skipped {not_found} — no existing rule)')


def main():
    parser = argparse.ArgumentParser(description="修正兜底类分类")
    parser.add_argument("--review", action="store_true", help="只打印 review 表")
    parser.add_argument("--apply", action="store_true", help="写入 DB")
    args = parser.parse_args()

    if args.review:
        review_table()
    elif args.apply:
        print('即将写入 DB：')
        apply_fixes()
    else:
        print('用法: --review  /  --apply')


if __name__ == "__main__":
    main()