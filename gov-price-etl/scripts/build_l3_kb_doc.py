def get_l3_desc(l3_code, l3_name):
    """获取 L3 标准描述, 优先查表, fallback 通用"""
    if l3_code in L3_STD_DESCS:
        return L3_STD_DESCS[l3_code]
    return f"属于 {l3_name} 类别"


def main():
    conn = sqlite3.connect(DB)
    cur = conn.cursor()

    # 1) 全部 L3 (从 category_v3 权威数据)
    l3_rows = cur.execute('''
        SELECT DISTINCT l1, l2, l3, name_l1, name_l2, name_l3
        FROM category_v3
        WHERE l3 IS NOT NULL AND l3 != ''
        ORDER BY l1, l2, l3
    ''').fetchall()

    # 不再读 breed_l3_map_v3 (原因: 现有数据受 Dify 错误分类污染)

    # 2) 渲染 markdown
    lines = []
    lines.append('# GB 50500 工程量清单规范 - v3 分类体系 知识库 (v2)')
    lines.append('')
    lines.append('> **用途**：材料品种 → L3 分类编码 语义检索')
    lines.append(f'> **规模**：8 个 L1 / 42 个 L2 / {len(l3_rows)} 个 L3')
    lines.append('> **数据源**：category_v3 表（GB 50854-2013 / GB/T 50856-2024 / GB 50857-2013 / GB 50858-2013 重建）')
    lines.append('> **v2 变更**：移除 "来自已积累映射" 段（之前含 Dify 错误分类如 岩棉板→02.04.08）')
    lines.append('')
    lines.append('## 检索说明')
    lines.append('')
    lines.append('- 每条 L3 是一个独立 chunk，含 L1/L2 路径 + 名称 + 标准描述')
    lines.append('- 检索时输入材料品种名，返回最相关的 top-K L3 编码')
    lines.append('- L3 编码格式 `XX.XX.XX`（如 01.05.15 钢筋工程）')
    lines.append('')

    # 按 L1/L2 分组
    l1_groups = defaultdict(lambda: defaultdict(list))
    for r in l3_rows:
        l1, l2, l3, n1, n2, n3 = r
        l1_groups[l1][l2].append((l3, n1, n2, n3))

    for l1 in sorted(l1_groups.keys()):
        first = l1_groups[l1][list(l1_groups[l1].keys())[0]][0]
        l1_name = first[1]
        lines.append('---')
        lines.append('')
        lines.append(f'## L1 {l1} {l1_name}')
        lines.append('')
        if l1 in L1_DESCS:
            lines.append(L1_DESCS[l1])
            lines.append('')

        for l2 in sorted(l1_groups[l1].keys()):
            first_l2 = l1_groups[l1][l2][0]
            l2_name = first_l2[2]
            lines.append(f'### L2 {l2} {l2_name}')
            lines.append('')

            for l3, n1, n2, n3 in l1_groups[l1][l2]:
                desc = get_l3_desc(l3, n3)
                lines.append(f'#### L3 {l3} {n3}')
                lines.append('')
                lines.append(f'**L3 编码**：`{l3}`')
                lines.append('')
                lines.append(f'**分类路径**：L1 {l1} {n1} → L2 {l2} {n2} → L3 {l3} {n3}')
                lines.append('')
                lines.append(f'**标准描述**：{desc}')
                lines.append('')

    # 附录: 速查表
    lines.append('---')
    lines.append('')
    lines.append('## 附录: 全部 L3 速查表')
    lines.append('')
    lines.append('| L3 编码 | L1 | L2 | L3 名称 | 标准描述 |')
    lines.append('|---|---|---|---|---|')
    for l1, l2, l3, n1, n2, n3 in l3_rows:
        desc = get_l3_desc(l3, n3).replace('|', '\\|')
        lines.append(f'| `{l3}` | {n1} | {n2} | {n3} | {desc} |')
    lines.append('')

    # 写文件
    content = '\n'.join(lines)
    OUT.write_text(content, encoding='utf-8')

    size_kb = OUT.stat().st_size / 1024
    print(f'生成 {OUT}')
    print(f'  字节: {OUT.stat().st_size} ({size_kb:.1f} KB)')
    print(f'  L1: {len(l1_groups)} 个')
    print(f'  L3 chunk: {len(l3_rows)} 个')
    print(f'  有标准描述的 L3: {sum(1 for r in l3_rows if r[2] in L3_STD_DESCS)} 个')
    print(f'  未描述 L3 (用通用): {sum(1 for r in l3_rows if r[2] not in L3_STD_DESCS)} 个')

    conn.close()


if __name__ == '__main__':
    main()
